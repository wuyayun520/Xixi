- (Byte *)MediaDataToCache:(Byte *)data {
    int lite = data[0];
    Byte block = data[1];
    int cropPublish = data[2];
    for (int i = cropPublish; i < cropPublish + lite; i++) {
        int value = data[i] - block;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
